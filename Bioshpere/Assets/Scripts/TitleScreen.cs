using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class TitleScreen : GameBase
{
    // Start is called before the first frame update
    void Start()
    {
        
    }

    // Update is called once per frame
    void Update()
    {
        if(Input.anyKeyDown)
        {
            Debug.Log("TitleScreen.Update: Input.anyKeyDown Event.  GameManager.eGameState = " +
                      gameManager.EGameState + ", calling GameManager.ShowTeamCountAndNamesMenuScreen");
            gameManager.ShowTeamCountAndNamesMenuScreen();
        }
    }

    



    

}
